# 1弟兄們，我還有話說，你們要靠主喜樂。我把這話再寫給你們，於我並不為難，於你們卻是妥當。 2應當防備犬類，防備作惡的，防備妄自行割的。 3因為真受割禮的，乃是我們這以神的靈敬拜、在基督耶穌裏誇口、不靠着肉體的。 4其實，我也可以靠肉體；若是別人想他可以靠肉體，我更可以靠着了。 5我第八天受割禮；我是以色列族、便雅憫支派的人，是希伯來人所生的希伯來人。就律法說，我是法利賽人； 6就熱心說，我是逼迫教會的；就律法上的義說，我是無可指摘的。 7只是我先前以為與我有益的，我現在因基督都當作有損的。 8不但如此，我也將萬事當作有損的，因我以認識我主基督耶穌為至寶。我為他已經丟棄萬事，看作糞土，為要得着基督； 9並且得以在他裏面，不是有自己因律法而得的義，乃是有信基督的義，就是因信神而來的義， 10使我認識基督，曉得他復活的大能，並且曉得和他一同受苦，效法他的死， 11或者我也得以從死裏復活。
