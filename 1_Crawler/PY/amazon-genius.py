# 基督再臨
# 6天使又對我說：「這些話是真實可信的。主就是眾先知被感之靈的神，差遣他的使者，將那必要快成的事指示他僕人。」 7「看哪，我必快來！凡遵守這書上預言的有福了！」
# 8這些事是我－約翰所聽見、所看見的；我既聽見、看見了，就在指示我的天使腳前俯伏要拜他。 9他對我說：「千萬不可！我與你和你的弟兄眾先知，並那些守這書上言語的人，同是作僕人的。你要敬拜神。」 10他又對我說：「不可封了這書上的預言，因為日期近了。 11不義的，叫他仍舊不義；污穢的，叫他仍舊污穢；為義的，叫他仍舊為義；聖潔的，叫他仍舊聖潔。」
# 12「看哪，我必快來！賞罰在我，要照各人所行的報應他。 13我是阿拉法，我是俄梅戛；我是首先的，我是末後的；我是初，我是終。」
