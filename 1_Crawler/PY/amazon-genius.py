# 8又有第二位天使接着說：「叫萬民喝邪淫、大怒之酒的巴比倫大城傾倒了！傾倒了！」
# 9又有第三位天使接着他們，大聲說：「若有人拜獸和獸像，在額上或在手上受了印記， 10這人也必喝神大怒的酒；此酒斟在神忿怒的杯中純一不雜。他要在聖天使和羔羊面前，在火與硫磺之中受痛苦。 11他受痛苦的煙往上冒，直到永永遠遠。那些拜獸和獸像，受牠名之印記的，晝夜不得安寧。」 12聖徒的忍耐就在此；他們是守神誡命和耶穌真道的。
# 13我聽見從天上有聲音說：「你要寫下：從今以後，在主裏面而死的人有福了！」聖靈說：「是的，他們息了自己的勞苦，做工的果效也隨着他們。」
# 地上的收割
# 14我又觀看，見有一片白雲，雲上坐着一位好像人子，頭上戴着金冠冕，手裏拿着快鐮刀。 15又有一位天使從殿中出來，向那坐在雲上的大聲喊着說：「伸出你的鐮刀來收割；因為收割的時候已經到了，地上的莊稼已經熟透了。」 16那坐在雲上的，就把鐮刀扔在地上，地上的莊稼就被收割了。
# 17又有一位天使從天上的殿中出來，他也拿着快鐮刀。 18又有一位天使從祭壇中出來，是有權柄管火的，向拿着快鐮刀的大聲喊着說：「伸出快鐮刀來，收取地上葡萄樹的果子，因為葡萄熟透了！」 19那天使就把鐮刀扔在地上，收取了地上的葡萄，丟在神忿怒的大酒醡中。 20那酒醡踹在城外，就有血從酒醡裏流出來，高到馬的嚼環，遠有六百里。
