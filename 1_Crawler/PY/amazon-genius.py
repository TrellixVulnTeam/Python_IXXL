# 1因此，我－保羅為你們外邦人作了基督耶穌被囚的，替你們祈禱此句是照對十四節所加。 2諒必你們曾聽見神賜恩給我，將關切你們的職分託付我， 3用啟示使我知道福音的奧祕，正如我以前略略寫過的。 4你們念了，就能曉得我深知基督的奧祕。 5這奧祕在以前的世代沒有叫人知道，像如今藉着聖靈啟示他的聖使徒和先知一樣。 6這奧祕就是外邦人在基督耶穌裏，藉着福音，得以同為後嗣，同為一體，同蒙應許。 7我作了這福音的執事，是照神的恩賜，這恩賜是照他運行的大能賜給我的。 8我本來比眾聖徒中最小的還小，然而他還賜我這恩典，叫我把基督那測不透的豐富傳給外邦人， 9又使眾人都明白，這歷代以來隱藏在創造萬物之神裏的奧祕是如何安排的， 10為要藉着教會使天上執政的、掌權的，現在得知神百般的智慧。 11這是照神從萬世以前，在我們主基督耶穌裏所定的旨意。 12我們因信耶穌，就在他裏面放膽無懼，篤信不疑地來到神面前。 13所以，我求你們不要因我為你們所受的患難喪膽，這原是你們的榮耀。
