# 12這不是說我已經得着了，已經完全了；我乃是竭力追求，或者可以得着基督耶穌所以得着我的所以得着我的：或譯所要我得的。 13弟兄們，我不是以為自己已經得着了；我只有一件事，就是忘記背後，努力面前的， 14向着標竿直跑，要得神在基督耶穌裏從上面召我來得的獎賞。 15所以我們中間，凡是完全人總要存這樣的心；若在甚麼事上存別樣的心，神也必以此指示你們。 16然而，我們到了甚麼地步，就當照着甚麼地步行。
# 17弟兄們，你們要一同效法我，也當留意看那些照我們榜樣行的人。 18因為有許多人行事是基督十字架的仇敵。我屢次告訴你們，現在又流淚地告訴你們： 19他們的結局就是沉淪；他們的神就是自己的肚腹。他們以自己的羞辱為榮耀，專以地上的事為念。 20我們卻是天上的國民，並且等候救主，就是主耶穌基督從天上降臨。 21他要按着那能叫萬有歸服自己的大能，將我們這卑賤的身體改變形狀，和他自己榮耀的身體相似。
