# 1我所親愛、所想念的弟兄們，你們就是我的喜樂，我的冠冕。我親愛的弟兄，你們應當靠主站立得穩。
# 保羅的勸導
# 2我勸友阿蝶和循都基，要在主裏同心。 3我也求你這真實同負一軛的，幫助這兩個女人，因為她們在福音上曾與我一同勞苦；還有革利免，並其餘和我一同做工的，他們的名字都在生命冊上。 4你們要靠主常常喜樂。我再說，你們要喜樂。 5當叫眾人知道你們謙讓的心。主已經近了。 6應當一無掛慮，只要凡事藉着禱告、祈求，和感謝，將你們所要的告訴神。 7神所賜、出人意外的平安必在基督耶穌裏保守你們的心懷意念。
# 8弟兄們，我還有未盡的話：凡是真實的、可敬的、公義的、清潔的、可愛的、有美名的，若有甚麼德行，若有甚麼稱讚，這些事你們都要思念。 9你們在我身上所學習的，所領受的，所聽見的，所看見的，這些事你們都要去行，賜平安的神就必與你們同在。
# 為餽送而致謝
# 10我靠主大大地喜樂，因為你們思念我的心如今又發生；你們向來就思念我，只是沒得機會。 11我並不是因缺乏說這話；我無論在甚麼景況都可以知足，這是我已經學會了。 12我知道怎樣處卑賤，也知道怎樣處豐富；或飽足，或飢餓；或有餘，或缺乏，隨事隨在，我都得了秘訣。 13我靠着那加給我力量的，凡事都能做。 14然而，你們和我同受患難原是美事。 15腓立比人哪，你們也知道我初傳福音離了馬其頓的時候，論到授受的事，除了你們以外，並沒有別的教會供給我。 16就是我在帖撒羅尼迦，你們也一次兩次地打發人供給我的需用。 17我並不求甚麼餽送，所求的就是你們的果子漸漸增多，歸在你們的帳上。 18但我樣樣都有，並且有餘。我已經充足，因我從以巴弗提受了你們的餽送，當作極美的香氣，為神所收納、所喜悅的祭物。 19我的神必照他榮耀的豐富，在基督耶穌裏，使你們一切所需用的都充足。 20願榮耀歸給我們的父神，直到永永遠遠。阿們！
