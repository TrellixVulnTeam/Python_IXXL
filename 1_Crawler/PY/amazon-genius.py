# 天上的敬拜
# 1此後，我觀看，見天上有門開了。我初次聽見好像吹號的聲音，對我說：「你上到這裏來，我要將以後必成的事指示你。」 2我立刻被聖靈感動，見有一個寶座安置在天上，又有一位坐在寶座上。 3看那坐着的，好像碧玉和紅寶石；又有虹圍着寶座，好像綠寶石。 4寶座的周圍又有二十四個座位；其上坐着二十四位長老，身穿白衣，頭上戴着金冠冕。 5有閃電、聲音、雷轟從寶座中發出；又有七盞火燈在寶座前點着；這七燈就是神的七靈。 6寶座前好像一個玻璃海，如同水晶。
# 寶座中和寶座周圍有四個活物，前後遍體都滿了眼睛。 7第一個活物像獅子，第二個像牛犢，第三個臉面像人，第四個像飛鷹。 8四活物各有六個翅膀，遍體內外都滿了眼睛。他們晝夜不住地說：
# 聖哉！聖哉！聖哉！
# 主神是昔在、今在、
# 以後永在的全能者。
