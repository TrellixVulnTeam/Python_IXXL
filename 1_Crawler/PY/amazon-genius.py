# 昔在、今在的聖者啊，
# 你這樣判斷是公義的；
# 6他們曾流聖徒與先知的血，
# 現在你給他們血喝；
# 這是他們所該受的。
# 7我又聽見祭壇中有聲音說：
# 是的，主神－全能者啊，
# 你的判斷義哉！誠哉！
# 8第四位天使把碗倒在日頭上，叫日頭能用火烤人。 9人被大熱所烤，就褻瀆那有權掌管這些災的神之名，並不悔改將榮耀歸給神。
# 10第五位天使把碗倒在獸的座位上，獸的國就黑暗了。人因疼痛就咬自己的舌頭； 11又因所受的疼痛，和生的瘡，就褻瀆天上的神，並不悔改所行的。
