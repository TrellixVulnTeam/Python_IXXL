# 騎白馬者
# 11我觀看，見天開了。有一匹白馬，騎在馬上的稱為誠信真實，他審判，爭戰，都按着公義。 12他的眼睛如火焰，他頭上戴着許多冠冕；又有寫着的名字，除了他自己沒有人知道。 13他穿着濺了血的衣服；他的名稱為神之道。 14在天上的眾軍騎着白馬，穿着細麻衣，又白又潔，跟隨他。 15有利劍從他口中出來，可以擊殺列國。他必用鐵杖轄管轄管：原文是牧他們，並要踹全能神烈怒的酒醡。 16在他衣服和大腿上有名寫着說：「萬王之王，萬主之主。」
# 17我又看見一位天使站在日頭中，向天空所飛的鳥大聲喊着說：「你們聚集來赴神的大筵席， 18可以吃君王與將軍的肉，壯士與馬和騎馬者的肉，並一切自主的為奴的，以及大小人民的肉。」 19我看見那獸和地上的君王，並他們的眾軍都聚集，要與騎白馬的並他的軍兵爭戰。 20那獸被擒拿；那在獸面前曾行奇事、迷惑受獸印記和拜獸像之人的假先知，也與獸同被擒拿。他們兩個就活活地被扔在燒着硫磺的火湖裏； 21其餘的被騎白馬者口中出來的劍殺了；飛鳥都吃飽了他們的肉。
