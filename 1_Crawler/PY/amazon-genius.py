# 婦人和龍
# 1天上現出大異象來：有一個婦人身披日頭，腳踏月亮，頭戴十二星的冠冕。 2她懷了孕，在生產的艱難中疼痛呼叫。 3天上又現出異象來：有一條大紅龍，七頭十角；七頭上戴着七個冠冕。 4牠的尾巴拖拉着天上星辰的三分之一，摔在地上。龍就站在那將要生產的婦人面前，等她生產之後，要吞吃她的孩子。 5婦人生了一個男孩子，是將來要用鐵杖轄管轄管：原文是牧萬國的；她的孩子被提到神寶座那裏去了。 6婦人就逃到曠野，在那裏有神給她預備的地方，使她被養活一千二百六十天。
# 7在天上就有了爭戰。米迦勒同他的使者與龍爭戰，龍也同牠的使者去爭戰， 8並沒有得勝，天上再沒有牠們的地方。 9大龍就是那古蛇，名叫魔鬼，又叫撒但，是迷惑普天下的。牠被摔在地上，牠的使者也一同被摔下去。 10我聽見在天上有大聲音說：「我神的救恩、能力、國度，並他基督的權柄，現在都來到了！因為那在我們神面前晝夜控告我們弟兄的，已經被摔下去了。 11弟兄勝過牠，是因羔羊的血和自己所見證的道。他們雖至於死，也不愛惜性命。 12所以，諸天和住在其中的，你們都快樂吧！只是地與海有禍了！因為魔鬼知道自己的時候不多，就氣忿忿地下到你們那裏去了。」
# 13龍見自己被摔在地上，就逼迫那生男孩子的婦人。 14於是有大鷹的兩個翅膀賜給婦人，叫她能飛到曠野，到自己的地方，躲避那蛇；她在那裏被養活一載二載半載。 15蛇就在婦人身後，從口中吐出水來，像河一樣，要將婦人沖去。 16地卻幫助婦人，開口吞了從龍口吐出來的水原文是河。 17龍向婦人發怒，去與她其餘的兒女爭戰，這兒女就是那守神誡命、為耶穌作見證的。 18那時龍就站在海邊的沙上。
