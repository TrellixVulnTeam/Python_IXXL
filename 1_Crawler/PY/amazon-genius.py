# 地上和天上的聖所
# 1原來前約有禮拜的條例和屬世界的聖幕。 2因為有預備的帳幕，頭一層叫作聖所，裏面有燈臺、桌子，和陳設餅。 3第二幔子後又有一層帳幕，叫作至聖所， 4有金香爐爐：或譯壇，有包金的約櫃，櫃裏有盛嗎哪的金罐和亞倫發過芽的杖，並兩塊約版； 5櫃上面有榮耀基路伯的影罩着施恩施恩：原文是蔽罪座。這幾件我現在不能一一細說。
# 6這些物件既如此預備齊了，眾祭司就常進頭一層帳幕，行拜神的禮。 7至於第二層帳幕，惟有大祭司一年一次獨自進去，沒有不帶着血為自己和百姓的過錯獻上。 8聖靈用此指明，頭一層帳幕仍存的時候，進入至聖所的路還未顯明。 9那頭一層帳幕作現今的一個表樣，所獻的禮物和祭物，就着良心說，都不能叫禮拜的人得以完全。 10這些事，連那飲食和諸般洗濯的規矩，都不過是屬肉體的條例，命定到振興的時候為止。
# 11但現在基督已經來到，作了將來美事的大祭司，經過那更大更全備的帳幕，不是人手所造，也不是屬乎這世界的； 12並且不用山羊和牛犢的血，乃用自己的血，只一次進入聖所，成了永遠贖罪的事。 13若山羊和公牛的血，並母牛犢的灰，灑在不潔的人身上，尚且叫人成聖，身體潔淨， 14何況基督藉着永遠的靈，將自己無瑕無疵獻給神，他的血豈不更能洗淨你們的心原文是良心，除去你們的死行，使你們事奉那永生神嗎？
